import { NavLink, useLocation } from 'react-router-dom';
import { LiquidGlassCard } from './LiquidGlassCard';
import { isTelegram } from '../lib/telegram';
import './BottomNav.css';

const HomeIcon = ({ className }: { className?: string }) => (
  <svg className={className} width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
    <path d="M3 9l9-7 9 7v11a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2z" />
    <polyline points="9 22 9 12 15 12 15 22" />
  </svg>
);

const SetsIcon = ({ className }: { className?: string }) => (
  <svg className={className} width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
    <rect x="2" y="3" width="20" height="14" rx="2" ry="2" />
    <line x1="8" y1="21" x2="16" y2="21" />
    <line x1="12" y1="17" x2="12" y2="21" />
  </svg>
);

const SettingsIcon = ({ className }: { className?: string }) => (
  <svg className={className} width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
    <circle cx="12" cy="12" r="3" />
    <path d="M19.4 15a1.65 1.65 0 0 0 .33 1.82l.06.06a2 2 0 0 1 0 2.83 2 2 0 0 1-2.83 0l-.06-.06a1.65 1.65 0 0 0-1.82-.33 1.65 1.65 0 0 0-1 1.51V21a2 2 0 0 1-2 2 2 2 0 0 1-2-2v-.09A1.65 1.65 0 0 0 9 19.4a1.65 1.65 0 0 0-1.82.33l-.06.06a2 2 0 0 1-2.83 0 2 2 0 0 1 0-2.83l.06-.06A1.65 1.65 0 0 0 4.68 15a1.65 1.65 0 0 0-1.51-1H3a2 2 0 0 1-2-2 2 2 0 0 1 2-2h.09A1.65 1.65 0 0 0 4.6 9a1.65 1.65 0 0 0-.33-1.82l-.06-.06a2 2 0 0 1 0-2.83 2 2 0 0 1 2.83 0l.06.06A1.65 1.65 0 0 0 9 4.68a1.65 1.65 0 0 0 1-1.51V3a2 2 0 0 1 2-2 2 2 0 0 1 2 2v.09a1.65 1.65 0 0 0 1 1.51 1.65 1.65 0 0 0 1.82-.33l.06-.06a2 2 0 0 1 2.83 0 2 2 0 0 1 0 2.83l-.06.06A1.65 1.65 0 0 0 19.4 9a1.65 1.65 0 0 0 1.51 1H21a2 2 0 0 1 2 2 2 2 0 0 1-2 2h-.09a1.65 1.65 0 0 0-1.51 1z" />
  </svg>
);

const tabs = [
  { path: '/', label: 'Home', Icon: HomeIcon },
  { path: '/sets', label: 'Sets', Icon: SetsIcon },
  { path: '/settings', label: 'Settings', Icon: SettingsIcon },
] as const;

export function BottomNav() {
  const location = useLocation();
  const pathname = location.pathname;
  const isTelegramEnv = isTelegram();

  return (
    <nav
      className={`bottom-nav ${isTelegramEnv ? 'bottom-nav--telegram' : ''}`}
      role="navigation"
      data-telegram={isTelegramEnv}
    >
      <LiquidGlassCard className="bottom-nav__container">
        {tabs.map(({ path, label, Icon }) => {
          const isActive = path === '/'
            ? pathname === '/'
            : pathname.startsWith(path);
          return (
            <NavLink
              key={path}
              to={path}
              className={() => `bottom-nav__item ${isActive ? 'bottom-nav__item--active' : ''}`}
              end={path === '/'}
            >
              <span className="bottom-nav__icon">
                <Icon />
              </span>
              <span className="bottom-nav__label">{label}</span>
            </NavLink>
          );
        })}
      </LiquidGlassCard>
    </nav>
  );
}
